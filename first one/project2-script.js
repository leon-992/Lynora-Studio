// CONTACT FORM
// ============================================
const contactForm = document.getElementById('contactForm');

if (contactForm) {
    contactForm.addEventListener('submit', function(e) {
        e.preventDefault();
        
        // Get form data
        const formData = new FormData(this);
        const name = formData.get('name');
        const email = formData.get('email');
        const phone = formData.get('phone');
        const subject = formData.get('subject');
        const message = formData.get('message');
        
        // Create mailto link
        const subjectLine = subject ? encodeURIComponent(subject) : encodeURIComponent('Anfrage von ' + name);
        const body = encodeURIComponent(
            `Name: ${name}\n` +
            `E-Mail: ${email}\n` +
            (phone ? `Telefon: ${phone}\n` : '') +
            `\nNachricht:\n${message}`
        );
        
        const mailtoLink = `mailto:info@farbora-malerbetrieb.de?subject=${subjectLine}&body=${body}`;
        
        // Open email client
        window.location.href = mailtoLink;
        
        // Show success message
        setTimeout(() => {
            alert('Vielen Dank für Ihre Nachricht! Ihr E-Mail-Programm sollte sich gleich öffnen.');
            this.reset();
        }, 100);
    });
}

